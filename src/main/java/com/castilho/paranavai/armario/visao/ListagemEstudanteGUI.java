    private void populaCorretoEstudante(List<Estudante> lista){
        DefaultTableModel modeloTabelaEstudante = (DefaultTableModel) tabelaEstudante.getModel();
        
        while (modeloTabelaEstudante.getRowCount() != 0){
            modeloTabelaEstudante.removeRow(0);
        }
        
        for (Estudante estudante : lista){
            Object[] dadosLinha = new Object[3];
            dadosLinha [0] = estudante.getPessoaId();
            dadosLinha [1] = estudante.getNome();
            dadosLinha [2] = estudante.getRa();
            modeloTabelaEstudante.addRow(dadosLinha);
        }
    }

